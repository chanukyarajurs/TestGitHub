package training;

public class Day1_Switch {
public static void main(String[] args){
	char ch='a';
	switch(ch)
	{
	case 'a' : System.out.println("Char a");
				break;
	case 'b' : System.out.println("Char b");
				break;
	default : System.out.println("Invalid Character");
	}

	String str = "Chanukya";
	switch (str){
	case "Chanukya" : System.out.println("Hi Welcome CHanu");
	}
	
}
}
